package com.rt.mylibrary.basebean;

public class PageStyleBean {
    private int height;
    private int width;
    private String size;
    private String direction;
    private String backgroundColor;
    private String backgroundRepeat;
    private String backgroundImageCrop;
    private String backgroundSize;
    private String backgroundImage;

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getDirection() {
        return direction;
    }

    public void setDirection(String direction) {
        this.direction = direction;
    }

    public String getBackgroundColor() {
        return backgroundColor;
    }

    public void setBackgroundColor(String backgroundColor) {
        this.backgroundColor = backgroundColor;
    }

    public String getBackgroundRepeat() {
        return backgroundRepeat;
    }

    public void setBackgroundRepeat(String backgroundRepeat) {
        this.backgroundRepeat = backgroundRepeat;
    }

    public String getBackgroundImageCrop() {
        return backgroundImageCrop;
    }

    public void setBackgroundImageCrop(String backgroundImageCrop) {
        this.backgroundImageCrop = backgroundImageCrop;
    }

    public String getBackgroundSize() {
        return backgroundSize;
    }

    public void setBackgroundSize(String backgroundSize) {
        this.backgroundSize = backgroundSize;
    }

    public String getBackgroundImage() {
        return backgroundImage;
    }

    public void setBackgroundImage(String backgroundImage) {
        this.backgroundImage = backgroundImage;
    }
}
